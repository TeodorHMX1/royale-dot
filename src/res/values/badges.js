const getBadge = (badgeID) => {
    const filteredBadges = royaleBadgesMapped.filter(badge => badge.id === badgeID)
    if (filteredBadges.length === 0) {
        return Badges.NoClan
    }
    const badge_name = filteredBadges[0].name
    const badge = Badges[badge_name]
    if (badge === undefined || badge === null) {
        return Badges.NoClan
    }
    return badge
}

